var app = angular.module('myOwnApp', ['ngRoute','myApp.services','BarcodeService','ui.utils','ui.bootstrap','reference','xeditable']);
app.config(['$routeProvider', '$locationProvider', '$httpProvider', function($routeProvider,$locationProvider,$httpProvider){
  $routeProvider
  .when('/home', {
    templateUrl:'view/home.html',
    controller:'myCtrl'
  })
.when('/barcode',{
  templateUrl:'view/payroll/barcodeMain.html',
  controller:'barcodeController'
})
.when('/student',{
  templateUrl:'view/studentLogin.html',
  controller:'myCtrl'
})
.when('/addStudent',{
  templateUrl:'view/payroll/addStudent.html',
  controller:'barcodeController'
})
.when('/editStudent',{
	templateUrl:'view/payroll/editStudent.html',
	controller:'barcodeEditController'
})
.when('/barcodeScan',{
  templateUrl:'view/payroll/scan.html',
  controller:'barcodeController'
})
.otherwise({
  redirectTo:'/home'
});
 $locationProvider.hashPrefix('');
}]);
